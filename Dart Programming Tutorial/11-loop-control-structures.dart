void main() {
  for (var greeting = 0; greeting < 10; greeting++) {
    var greeting = "Hello World";
    print(greeting);
  }
}

// Iterators syntax 

/* 1. For loop - DEFINITE LOOP (If you know how many times your loop is going to run)

  for (var i = 0; i < 4; i++) {
    print("Hello");
  }

 */

/* 2. While loop - INDEFINITE LOOP

  while(i < 4) {
    print("Hello");
    i++;
  }
  
 */

/* 3. Do While loop - INDEFINITE LOOP

  do {
    print("Hello");
    i++;
  } while (i < 4);
  
 */